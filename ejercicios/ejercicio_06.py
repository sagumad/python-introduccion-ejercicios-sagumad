# sexto ejercicio (la demostracion) de como son las 4 tipos de estructuras principales de python

# 1. Listas
colores = ["vinotinto", "negro mate", "cian"]
print("Lista de mis colores favoritos:", colores)
print("Tipo de estructura:", type(colores))


# 2. Tuplas
coordenadas = (8303, 55)
print("Tupla de coordenadas geográficas:", coordenadas)
print("Tipo de estructura:", type(coordenadas))


# 3. Conjunto
frutas = {"arandanos", "ochuas", "lychee"}
print("Conjunto de frutas:", frutas)
print("Tipo de estructura:", type(frutas))


